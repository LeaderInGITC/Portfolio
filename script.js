function openRightMenu() {
  document.getElementById("mediaMenuOnClick").style.top = "100px";
  document.getElementById("mediaMenuOnClick").style.opacity = "1";
  document.getElementById("first").style.opacity = "0";
  
}
function closeRightMenu() {
  document.getElementById("mediaMenuOnClick").style.top = "-400px";
  document.getElementById("mediaMenuOnClick").style.opacity = "0";
  document.getElementById("first").style.opacity = "1";

}
